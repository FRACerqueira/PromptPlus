namespace PromptPlusLibrary
{
    /// <summary>
    /// Represents The Styles Switch control.
    /// This enum defines various regions or components of the Switch Control.  
    /// </summary>
    public enum SwitchStyles
    {
        /// <summary>
        /// Prompt Region
        /// </summary>
        Prompt,
        /// <summary>
        /// Answer Region
        /// </summary>
        Answer,
        /// <summary>
        /// Description Region
        /// </summary>
        Description,
        /// <summary>
        /// Tooltips Region
        /// </summary>
        Tooltips,
        /// <summary>
        /// Slider Region
        /// </summary>
        Slider,
        /// <summary>
        /// Ranger Region
        /// </summary>
        Ranger,
        /// <summary>
        /// Slider Region when state 'On'
        /// </summary>
        SliderOn,
        /// <summary>
        /// Slider Region when state 'Off'
        /// </summary>
        SliderOff,
        /// <summary>
        /// Error Region
        /// </summary>
        Error
    }
}
