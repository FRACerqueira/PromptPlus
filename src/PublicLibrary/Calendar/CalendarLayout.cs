namespace PromptPlusLibrary
{
    /// <summary>
    /// Represents the layout options for a calendar.
    /// </summary>
    public enum CalendarLayout
    {
        /// <summary>
        /// Single line grid layout.
        /// </summary>
        SingleGrid,

        /// <summary>
        /// Double line grid layout.
        /// </summary>
        DoubleGrid,

        /// <summary>
        /// ASCII single line grid layout.
        /// </summary>
        AsciiSingleGrid,

        /// <summary>
        /// ASCII double line grid layout.
        /// </summary>
        AsciiDoubleGrid,
    }
}
